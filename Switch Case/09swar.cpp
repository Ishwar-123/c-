#include<iostream>
using namespace std;
int main(){
	char name;
	cout<<"Enter Any Alphabate :- ";
	cin>>name;
	switch(name){
		case'A':
		 cout<<"A is Swar;";
		 break;
		 case'E':
		 cout<<"E is Swar;";
		 break;
		 case'I':
		 cout<<"I is Swar;";
		 break;
		 case'O':
		 cout<<"O is Swar;";
		 break;
		 case'U':
		 cout<<"U is Swar;";
		 break;
		 default:
		 	cout<<name<<" is Not Swar;";
	}
}
